package com.example.database;

public class CardBeanSch {
    private String name;
    private String date;
    private String time;
    private int pic;
    private String subject;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getPic() {
        return pic;
    }

    public void setPic(int pic) {
        this.pic = pic;
    }

    public String  getSubject() {
        return subject;
    }

    public void setSubject(String score) {
        this.subject = score;
    }
}


